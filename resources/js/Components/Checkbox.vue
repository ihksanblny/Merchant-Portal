<script setup>
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        required: true,
    },
    value: {
        default: null,
    },
});

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <input
        type="checkbox"
        :value="value"
        v-model="proxyChecked"
        class="rounded-[0.4rem] border-surface-low bg-surface text-brand-container shadow-sm focus:ring-[3px] focus:ring-brand-container/20 transition-all duration-300 w-5 h-5 cursor-pointer"
    />
</template>
